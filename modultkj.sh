Pengertian Komputer
-------------------
Apa yang dimaksud dengan komputer? Secara
umum, pengertian Komputer adalah suatu
perangkat elektronik yang dapat digunakan
untuk mengolah data sesuai dengan prosedur
yang telah dirumuskan sebelumnya sehingga
menghasilkan informasi bermanfaat bagi
penggunanya.

Definisi komputer adalah alat elektronik yang
terdiri dari rangkaian berbagai komponen yang
saling terhubung sehingga membentuk suatu
sistem kerja. Sistem di dalam komputer
tersebut dapat melakukan pekerjaan secara
otomatis berdasarkan program yang
diperintahkan kepadanya sehingga mampu
menghasilkan informasi berdasarkan data dan
program yang ada.

Pengertian Jaringan Komputer
----------------------------
Jaringan komputer (jaringan) adalah jaringan
telekomunikasi yang memungkinkan antar
komputer untuk saling berkomunikasi dengan
bertukar data. Tujuan dari jaringan komputer
adalah agar dapat mencapai tujuannya, setiap
bagian dari jaringan komputer dapat meminta
dan memberikan layanan (service). Pihak yang
meminta/menerima layanan disebut klien
(client) dan yang memberikan/mengirim
layanan disebut peladen (server). Desain ini
disebut dengan sistem client-server, dan
digunakan pada hampir seluruh
aplikasijaringan komputer.

Dua buah komputer yang masing-masing
memiliki sebuah kartu jaringan, kemudian
dihubungkan melalui kabel maupun
nirkabelsebagai medium transmisi data, dan
terdapat perangkat lunak sistem operasi
jaringan akan membentuk sebuah jaringan
komputer yang sederhana. Apabila ingin
membuat jaringan komputer yang lebih luas
lagi jangkauannya, maka diperlukan peralatan
tambahan seperti Hub, Bridge, Switch, Router,
Gateway sebagai peralatan interkoneksinya.

